import React, { useState } from 'react';
import { Card, CardContent, CardHeader, CardTitle } from '../../ui/card';
import { Button } from '../../ui/button';
import { Badge } from '../../ui/badge';
import { HelpCircle, ChevronDown, ChevronUp, Mail, MessageCircle } from 'lucide-react';

const FocusHeightFAQ: React.FC = () => {
  const [openItems, setOpenItems] = useState<number[]>([]);

  const toggleItem = (index: number) => {
    setOpenItems(prev => 
      prev.includes(index) 
        ? prev.filter(i => i !== index)
        : [...prev, index]
    );
  };

  const faqs = [
    {
      question: 'How accurate is the focus height calculator?',
      answer: `Our focus height calculator is typically accurate within ±0.5mm when input parameters match real operating conditions. Accuracy depends on:

• **Beam quality**: M² factor affects actual spot size
• **Lens condition**: Clean, undamaged lenses are critical
• **Material flatness**: Warped materials require adjustment
• **Calibration**: Regular system calibration improves accuracy

For critical applications, always validate with test cuts and adjust based on actual results.`,
      category: 'Calculator Accuracy'
    },
    {
      question: 'What focus height should I use for different cutting operations?',
      answer: `Focus height depends on your cutting objective:

**Through Cutting (Most Common):**
• Focus 1/3 into material thickness
• Example: 6mm steel → focus at +2mm from surface

**Surface Operations:**
• Focus on material surface (0mm)
• Best for engraving, marking, thin materials

**Thick Materials (>10mm):**
• Focus deeper, up to 60% of thickness
• Helps maintain power density through cut

**Quality Priority:**
• Adjust focus for top vs bottom edge quality
• Negative focus improves bottom edge
• Positive focus improves top edge`,
      category: 'Focus Guidelines'
    },
    {
      question: 'Why is my calculated focus height not working well?',
      answer: `Several factors can cause focus height issues:

**Common Causes:**
• **Beam alignment**: Misaligned beam affects focus position
• **Lens contamination**: Dirty lenses change focal characteristics
• **Material warping**: Uneven surfaces require height adjustment
• **Gas pressure**: Incorrect assist gas affects cutting dynamics

**Troubleshooting Steps:**
1. Check beam alignment and lens condition
2. Verify material is flat and properly supported
3. Confirm assist gas pressure and flow
4. Test with focus height adjustments (±0.5mm steps)
5. Consider material-specific factors (reflectivity, thickness)

**When to Adjust:**
• Poor edge quality → adjust focus position
• Incomplete cuts → increase power or adjust focus
• Excessive dross → optimize focus and speed`,
      category: 'Troubleshooting'
    }
  ];

  return (
    <Card>
      <CardHeader>
        <CardTitle className="flex items-center">
          <HelpCircle className="h-5 w-5 mr-2" />
          Frequently Asked Questions
        </CardTitle>
        <p className="text-sm text-gray-600">
          Common questions about focus height calculations and optimization
        </p>
      </CardHeader>
      <CardContent>
        <div className="space-y-4">
          {faqs.map((faq, index) => (
            <div key={index} className="border border-gray-200 rounded-lg">
              <button
                onClick={() => toggleItem(index)}
                className="w-full px-6 py-4 text-left flex items-center justify-between hover:bg-gray-50 transition-colors"
              >
                <div className="flex-1">
                  <div className="flex items-center space-x-2 mb-1">
                    <h3 className="font-medium text-gray-900">{faq.question}</h3>
                  </div>
                  <Badge variant="secondary" className="text-xs">
                    {faq.category}
                  </Badge>
                </div>
                {openItems.includes(index) ? (
                  <ChevronUp className="h-5 w-5 text-gray-400" />
                ) : (
                  <ChevronDown className="h-5 w-5 text-gray-400" />
                )}
              </button>
              
              {openItems.includes(index) && (
                <div className="px-6 pb-4">
                  <div className="prose prose-sm max-w-none text-gray-600">
                    {faq.answer.split('\n').map((paragraph, pIndex) => {
                      if (paragraph.trim() === '') return null;
                      
                      // Handle bold text
                      if (paragraph.includes('**')) {
                        const parts = paragraph.split('**');
                        return (
                          <p key={pIndex} className="mb-3">
                            {parts.map((part, partIndex) => 
                              partIndex % 2 === 1 ? (
                                <strong key={partIndex}>{part}</strong>
                              ) : (
                                part
                              )
                            )}
                          </p>
                        );
                      }
                      
                      // Handle bullet points
                      if (paragraph.startsWith('• ')) {
                        return (
                          <li key={pIndex} className="ml-4 mb-1">
                            {paragraph.substring(2)}
                          </li>
                        );
                      }
                      
                      // Handle numbered lists
                      if (/^\d+\./.test(paragraph.trim())) {
                        return (
                          <li key={pIndex} className="ml-4 mb-1">
                            {paragraph.trim()}
                          </li>
                        );
                      }
                      
                      return (
                        <p key={pIndex} className="mb-3">
                          {paragraph}
                        </p>
                      );
                    })}
                  </div>
                </div>
              )}
            </div>
          ))}
        </div>

        {/* Support Section */}
        <div className="mt-8 p-6 bg-blue-50 rounded-lg">
          <h3 className="font-semibold text-blue-900 mb-2">Still Have Questions?</h3>
          <p className="text-sm text-blue-800 mb-4">
            Our technical support team is here to help you optimize your focus height settings and improve your cutting operations.
          </p>
          
          <div className="flex flex-col sm:flex-row gap-3">
            <Button 
              variant="outline" 
              size="sm"
              className="flex-1"
              onClick={() => window.location.href = 'mailto:support@lasercalc.com?subject=Focus Height Calculator Question'}
            >
              <Mail className="h-4 w-4 mr-2" />
              Email Support
            </Button>
            
            <Button 
              variant="outline" 
              size="sm"
              className="flex-1"
              onClick={() => window.open('https://chat.lasercalc.com', '_blank')}
            >
              <MessageCircle className="h-4 w-4 mr-2" />
              Live Chat
            </Button>
            
            <Button 
              size="sm"
              className="flex-1"
              onClick={() => window.location.href = '/contact/technical-support'}
            >
              Contact Expert
            </Button>
          </div>
          
          <div className="mt-3 text-xs text-blue-700">
            <strong>Response Time:</strong> Within 4 hours during business hours (Mon-Fri, 8AM-6PM EST)
          </div>
        </div>
      </CardContent>
    </Card>
  );
};

export default FocusHeightFAQ;
